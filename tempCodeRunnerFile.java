public class HollowRohmbus {
    public static void main(String[] args){
        int i,j;
        for (i=1;i<=5;i++){
            for (j=1;j<=i;j++){
                System.out.print(" ");
        }
        for(int k=1;k<=5;k++){
            System.out.print(" *");}
        System.out.println();
       }
    }
    
}
